    # Initialize logging
    exec >> "$LOG_FILE" 2>&1

    show_header "GoalDone Dependency Installation"
    show_info "Starting dependency installation"

    # Step 1: Pre-flight validation
    if ! check_prerequisites; then
        show_error "Pre-flight validation failed. See $LOG_FILE for details."
        exit 1
    fi
    show_success "All prerequisites met."
    mark_step_complete 1 "Verify Prerequisites"

    show_info "Starting execution from Step 2 to Step $TOTAL_STEPS..."
    echo "" >&3

    # Step 2: Install Packages
    show_header "Step 2: Install Packages"
    step_install_packages

    # Step 3: Configure UFW (only if we reach here after install-packages pause)
    show_header "Step 3: Configure UFW"
    step_configure_ufw

    show_header "Installation Complete"
    show_success "All dependency installation steps completed!"
    show_info "Now run: bash deploy.sh"

    # Ensure non-root user can access state and log files
    ensure_file_permissions
}

main
